/**
 * Check if an asset is already mapped to Cloudinary
 * @param {string} localPath - Local path to the asset
 * @param {string} type - 'image' or 'video'
 * @returns {boolean} - Whether the asset is mapped
 */
export const isAssetMapped = (localPath, type = 'image') => {
  const assetMap = type === 'video' ? videoAssets : imageAssets;
  return !!assetMap[localPath];
};

/**
 * Get Cloudinary public ID for a local asset
 * @param {string} localPath - Local path to the asset
 * @param {string} type - 'image' or 'video'
 * @returns {string|null} - Cloudinary public ID or null if not mapped
 */
export const getCloudinaryId = (localPath, type = 'image') => {
  const assetMap = type === 'video' ? videoAssets : imageAssets;
  return assetMap[localPath] || null;
};